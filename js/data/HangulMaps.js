// 비트맵 → 플랫폼 배열 변환 (수평 병합 최적화)
function generateLetterPlatforms(charKey, mapSize) {
    const bitmap = HANGUL_BITMAPS[charKey];
    if (!bitmap) return [];

    const rows = bitmap.length;
    const cols = bitmap[0].length;
    const cellW = mapSize / cols;
    const cellH = mapSize / rows;
    const platH = HANGUL_MAP_CONFIG.platformHeight;
    const platforms = [];

    for (let row = 0; row < rows; row++) {
        const line = bitmap[row];
        let spanStart = -1;

        for (let col = 0; col <= cols; col++) {
            const filled = col < cols && line[col] === '#';

            if (filled && spanStart === -1) {
                spanStart = col;
            } else if (!filled && spanStart !== -1) {
                // 연속된 '#'을 하나의 플랫폼으로 병합
                platforms.push({
                    x: Math.round(spanStart * cellW),
                    y: Math.round(row * cellH),
                    w: Math.round((col - spanStart) * cellW),
                    h: platH,
                    isLetter: true,
                });
                spanStart = -1;
            }
        }
    }

    return platforms;
}
